<?php
// Correct way to use <br> as an HTML line break
$a = 15;
$b = 7;
echo "Sum: " . ($a + $b) . "<br>"; // This will print the sum and add a line break
echo "Difference: " . ($a - $b) . "<br>"; // Prints the difference and a line break
echo "Product: " . ($a * $b) . "<br>"; // Prints the product and a line break
echo "Quotient: " . ($a / $b) . "<br>"; // Prints the quotient and a line break
?>